
//function declaration (defining a function)
function square(number) { 
    return number * number; 
}


//a function call (calling or execution a function)

const result = square(5); 
console.log(result); 

//declaring and invoking functions 

//function expression 
const name = function(params) { 
    //statements 
}

//arrow function 
const name2 = (params) => { 
    //statements 
}


//MORE ABOUT ARROW FUNCTIONS 
const square = (number) => number * number; 

const result2 = square(2); 

console.log(result2); 


